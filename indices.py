cursos = ["python", "django", "c", "c++", "java", "php"]

curso = cursos[3]
print(curso)

#Penultimo de la lista
penultimo = cursos[-2]
print(penultimo)

#Ultimo de la lista
ultimo = cursos[-1]
print(ultimo)

#Sublista de los tres primeros elementos
sub = cursos[0:3]
print(sub)

#Restantes elementos
restantes = cursos[4:]
print(restantes)

#Desde:Hasta:Salto
nueva = cursos[1:6:2]
print(nueva)

#Lista inversa
inversa = cursos[::-1]
print(inversa)